package kosmo;

public class Variable {

	public static void main(String[] args) {
		int a=3;
		String b="화면";
		System.out.println(a);
		System.out.println(b);
		System.out.println("============");
		System.out.println(b+"출력");
		System.out.println(a+b+"출력");
		System.out.println("============");
		a=10;
		b="오늘";
		System.out.println(a);
		System.out.println(b);
		System.out.println("============");
		System.out.println(b+"출력");
		System.out.println(a+b+"출력");
	}

}
