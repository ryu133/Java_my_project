package ch01;

public class Ex02_ConsolOut {

	public static void main(String[] args) {
		System.out.printf("%d\n",30);//정수
		System.out.printf("%o\n",10);//8진수
		System.out.printf("%x\n",10);//16진수
		System.out.printf("%s\n","dd");//문자열
		System.out.printf("%f\n",10.2222);//실수
		System.out.printf("%4.2f\n",110.222);//소수점 이하 2자리까지
	}

}
